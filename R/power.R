#' @title Square a number
#'
#' @description Takes in any numeric value and squares it.
#'
#' @param x A numeric value to be squared.
#'
#' @return The square of input.
#' @export
#'
#' @examples square(2)
square <- function(x){
  return(x^2)
}

#' @title Cube a number
#'
#' @description Takes in any numeric value and cubes it.
#'
#' @param x A numeric value to be cubed.
#'
#' @return The cube of input.
#' @export
#'
#' @examples cube(3)
cube <- function(x){
  return(x^3)
}
